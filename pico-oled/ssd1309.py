import digitalio
import time

class SSD1309_SPI:
    def __init__(self, width, height, sck, sda, dc, res, cs):
        self.width = width
        self.height = height
        self.pages = height // 8
        self.buffer = bytearray(self.pages * width)

        for pin in (sck, sda, dc, res, cs):
            pin.direction = digitalio.Direction.OUTPUT

        self.sck = sck
        self.sda = sda
        self.dc = dc
        self.res = res
        self.cs = cs

        self.sck.value = False
        self.cs.value = True
        self.res.value = True

        self._reset()
        self._init()

    def _reset(self):
        self.res.value = True
        time.sleep(0.010)
        self.res.value = False
        time.sleep(0.010)
        self.res.value = True
        time.sleep(0.010)

    def _write_byte(self, b):
        for i in range(7, -1, -1):
            self.sda.value = bool(b & (1 << i))
            self.sck.value = True
            self.sck.value = False

    def _cmd(self, cmd):
        self.dc.value = False
        self.cs.value = False
        self._write_byte(cmd)
        self.cs.value = True

    def _data(self, buf):
        self.dc.value = True
        self.cs.value = False
        for b in buf:
            self._write_byte(b)
        self.cs.value = True

    def _init(self):
        for cmd in (
            0xAE,
            0xD5, 0x80,
            0xA8, 0x3F,
            0xD3, 0x00,
            0x40,
            0xA1,
            0xC8,
            0xDA, 0x12,
            0x81, 0xFF,
            0xD9, 0x22,
            0xDB, 0x30,
            0x20, 0x00,
            0xA4,
            0xA6,
            0xAF,
        ):
            self._cmd(cmd)
        self.fill(0)
        self.show()

    def show(self):
        self._cmd(0x21); self._cmd(0x00); self._cmd(self.width - 1)
        self._cmd(0x22); self._cmd(0x00); self._cmd(self.pages - 1)
        self._data(self.buffer)

    def fill(self, col):
        val = 0xFF if col else 0x00
        for i in range(len(self.buffer)):
            self.buffer[i] = val

    def pixel(self, x, y, col):
        if 0 <= x < self.width and 0 <= y < self.height:
            idx = (y // 8) * self.width + x
            bit = 1 << (y % 8)
            if col:
                self.buffer[idx] |= bit
            else:
                self.buffer[idx] &= ~bit

    def text(self, string, x, y, col=1):
        for i, char in enumerate(string):
            c = ord(char)
            if 32 <= c <= 126:
                off = (c - 32) * 8
                glyph = _FONT[off:off + 8]
                for row in range(8):
                    byte = glyph[row]
                    for bit in range(8):
                        if byte & (1 << bit):
                            self.pixel(x + i * 8 + bit, y + row, col)


