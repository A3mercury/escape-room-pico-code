def _fm_send(data, clk, lat, v0, v1, v2, v3, v4, v5, lat_start=52):
    lat.value = False
    for i in range(64):
        data[0].value = v0
        data[1].value = v1
        data[2].value = v2
        data[3].value = v3
        data[4].value = v4
        data[5].value = v5
        if i >= lat_start:
            lat.value = True
        clk.value = True
        clk.value = False
    lat.value = False

def _init_fm6126a():
    pins = [board.GP2, board.GP3, board.GP9, board.GP7, board.GP8, board.GP6]
    data = []
    for p in pins:
        pin = digitalio.DigitalInOut(p)
        pin.direction = digitalio.Direction.OUTPUT
        data.append(pin)
    clk = digitalio.DigitalInOut(board.GP11)
    clk.direction = digitalio.Direction.OUTPUT
    lat = digitalio.DigitalInOut(board.GP12)
    lat.direction = digitalio.Direction.OUTPUT

    _fm_send(data, clk, lat, True, True, True, True, True, True)
    _fm_send(data, clk, lat, False, True, False, False, True, False)

    for pin in data:
        pin.deinit()
    clk.deinit()
    lat.deinit()

