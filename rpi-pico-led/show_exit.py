# Timer sits in the clear bottom-left pocket (icon's left edge is at col 34).
# Scale 1 gives 27x7px for MM:SS — fits with room to spare.
T_X = 4   # left padding
T_Y = 53  # top of timer

def draw_timer(remaining):
    palette[1] = 0xFFFF00 if remaining < 1800 else 0x00FF00
    minutes = remaining // 60
    seconds = remaining % 60
    s = "{:02d}:{:02d}".format(minutes, seconds)

    # Clear timer area
    for y in range(T_Y, T_Y + 7):
        for x in range(T_X, T_X + 30):
            bitmap[x, y] = 0

    cx = T_X
    for ch in s:
        for row, bits in enumerate(FONT[ch]):
            for col in range(5):
                on = (bits >> (4 - col)) & 1
                px = cx + col
                py = T_Y + row
                if 0 <= px < 64 and 0 <= py < 64:
                    bitmap[px, py] = on
        cx += 4 if ch == ':' else 6

