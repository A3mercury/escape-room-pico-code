PAUSED, RUNNING = 0, 1
state           = PAUSED
pause_remaining = COUNTDOWN_FROM
end_time        = 0.0
last            = -1
last_start_press = 0.0

prev_add   = True
prev_sub   = True
prev_start = True

while True:
    now = time.monotonic()

    # Current remaining depends on state
    if state == RUNNING:
        remaining = max(0, int(end_time - now))
        if remaining == 0:
            state = PAUSED
            pause_remaining = 0
    else:
        remaining = pause_remaining

    if remaining != last:
        draw_timer(remaining)
        last = remaining

    cur_add   = btn_add.value
    cur_sub   = btn_sub.value
    cur_start = btn_start.value

    # +1 min — falling edge, cap at 60:00
    if prev_add and not cur_add:
        if state == RUNNING:
            end_time = min(now + COUNTDOWN_FROM, end_time + 60)
        else:
            pause_remaining = min(COUNTDOWN_FROM, pause_remaining + 60)

    # -1 min — falling edge
    if prev_sub and not cur_sub:
        if state == RUNNING:
            end_time = max(now, end_time - 60)
        else:
            pause_remaining = max(0, pause_remaining - 60)

    # Start / pause / reset — falling edge
    if prev_start and not cur_start:
        if now - last_start_press < 0.4:
            # Double press: reset to 60:00, return to paused
            state = PAUSED
            pause_remaining = COUNTDOWN_FROM
            last_start_press = 0.0
        else:
            # Single press: toggle running/paused
            if state == RUNNING:
                pause_remaining = max(0, int(end_time - now))
                state = PAUSED
            else:
                end_time = now + pause_remaining
                state = RUNNING
            last_start_press = now

    prev_add   = cur_add
    prev_sub   = cur_sub
    prev_start = cur_start

    time.sleep(0.05)
